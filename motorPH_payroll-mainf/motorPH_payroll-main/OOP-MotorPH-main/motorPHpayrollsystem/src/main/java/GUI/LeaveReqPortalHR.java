    private void loadLeaveRequests() {
        DefaultTableModel model = (DefaultTableModel) leaveTable.getModel();
        model.setRowCount(0);
        
        try {
            LeaveService leaveService = new LeaveService();
            List<String[]> allRequests = leaveService.getAllLeaveRequests();
            
            for (String[] request : allRequests) {
                if (request.length >= 5) {
                    model.addRow(new Object[]{
                        request[0].trim(), // Employee Number
                        request[1].trim(), // Leave Type
                        request[2].trim(), // Start Date
                        request[3].trim(), // End Date
                        request[4].trim()  // Status
                    });
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading leave requests: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
}
    
    
