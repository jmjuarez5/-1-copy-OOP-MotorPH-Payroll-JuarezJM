    private void rejectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rejectButtonActionPerformed
        int selectedRow = leaveTable.getSelectedRow();
        if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a leave request to reject.", "No Selection", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String employeeNumber = (String) leaveTable.getValueAt(selectedRow, 0);
    String leaveType = (String) leaveTable.getValueAt(selectedRow, 1);
    String startDate = (String) leaveTable.getValueAt(selectedRow, 2);
    String endDate = (String) leaveTable.getValueAt(selectedRow, 3);
    String status = (String) leaveTable.getValueAt(selectedRow, 4);

    if (!"Pending".equals(status)) {
        JOptionPane.showMessageDialog(this, "This leave request has already been processed.", "Already Processed", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this,
        "Are you sure you want to reject this leave request?",
        "Confirm Rejection",
        JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        updateLeaveStatus(employeeNumber, leaveType, startDate, endDate, "Rejected");
        loadLeaveRequests();
        JOptionPane.showMessageDialog(this, "Leave request rejected successfully!");
    }
    }//GEN-LAST:event_rejectButtonActionPerformed

private void updateLeaveStatus(String employeeNumber, String leaveType, String startDate, String endDate, String newStatus) {
        try {
            LeaveService leaveService = new LeaveService();
            boolean success = false;
            
            if ("Approved".equals(newStatus)) {
                success = leaveService.approveLeaveRequest(employeeNumber, leaveType, startDate, endDate);
            } else if ("Rejected".equals(newStatus)) {
                success = leaveService.rejectLeaveRequest(employeeNumber, leaveType, startDate, endDate);
            }
            
            if (!success) {
                JOptionPane.showMessageDialog(this, "Failed to update leave request status.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Validation Error", JOptionPane.WARNING_MESSAGE);
        } catch (IllegalStateException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Status Error", JOptionPane.WARNING_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error updating leave request: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

    
