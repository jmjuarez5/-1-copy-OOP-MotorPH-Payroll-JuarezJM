    private void timeInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timeInActionPerformed
        try {
            if (currentEmployee == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "No employee loaded", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            String empId = currentEmployee.getEmployeeNumber();
            String date = getCurrentDate();
            String timeIn = getCurrentTime();
            
            DAO.AttendanceDAO attendanceDAO = new DAO.AttendanceDAO();
            attendanceDAO.logAttendance(empId, date, timeIn, "");
            
            javax.swing.JOptionPane.showMessageDialog(this, "Time-In recorded successfully!", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error recording time-in: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_timeInActionPerformed

    private void timeOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timeOutActionPerformed
        try {
            if (currentEmployee == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "No employee loaded", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            String empId = currentEmployee.getEmployeeNumber();
            String date = getCurrentDate();
            String timeOut = getCurrentTime();
            
            DAO.AttendanceDAO attendanceDAO = new DAO.AttendanceDAO();
            attendanceDAO.logAttendance(empId, date, "", timeOut);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Time-Out recorded successfully!", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error recording time-out: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_timeOutActionPerformed

    // Helper methods for date/time
    private String getCurrentDate() {
        java.time.LocalDate today = java.time.LocalDate.now();
        return today.toString(); // YYYY-MM-DD format
    }

    private String getCurrentTime() {
        java.time.LocalTime now = java.time.LocalTime.now();
        // Remove nanoseconds for cleaner CSV format
        return now.truncatedTo(java.time.temporal.ChronoUnit.SECONDS).toString(); // HH:MM:SS format
    }

