    /**
     * Load employee details using service layer.
     * @param employeeId the employee ID
     */
    public void loadEmployeeDetails(String employeeId) {
        try {
            CTRL_SRVS.EmployeeService employeeService = new CTRL_SRVS.EmployeeService();
            Employee employee = employeeService.getEmployeeDetails(employeeId);
            if (employee != null) {
                fillEmployeeDetails(employee);
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Employee not found: " + employeeId, 
                                                           "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading employee details: " + e.getMessage(), 
                                                       "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
    
