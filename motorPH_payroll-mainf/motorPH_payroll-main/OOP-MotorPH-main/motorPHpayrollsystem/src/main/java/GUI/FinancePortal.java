    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        String searchTerm = searchField.getText().trim();
        if (!searchTerm.isEmpty()) {
            DefaultTableModel model = (DefaultTableModel) empInfoTable.getModel();
            int foundIndex = financeService.searchEmployee(model, searchTerm);
            
            if (foundIndex != -1) {
                // Select the matching row and scroll to it
                empInfoTable.getSelectionModel().setSelectionInterval(foundIndex, foundIndex);
                empInfoTable.scrollRectToVisible(empInfoTable.getCellRect(foundIndex, 0, true));
            } else {
                JOptionPane.showMessageDialog(this, "Employee not found!", "Search Result", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please enter an Employee ID, Last Name, or First Name to search.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_searchButtonActionPerformed

