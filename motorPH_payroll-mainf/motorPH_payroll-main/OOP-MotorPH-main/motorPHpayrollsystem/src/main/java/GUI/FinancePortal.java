    /**
     * Filters the workHoursTable to show only rows matching the selected month.
     * If no employee is selected, the table remains unchanged.
     * 
     * @param employeeID The employee ID to filter by
     */
    private void filterWorkHoursTable(String employeeID) {
        DefaultTableModel model = (DefaultTableModel) workHoursTable.getModel();
        String selectedMonthName = (String) selectedMonth.getSelectedItem();
        int monthIndex = selectedMonth.getSelectedIndex() + 1; // 1-12
        
        // Clear existing rows
        model.setRowCount(0);
        
        // Reload attendance data and filter by month
        try {
            String[][] allData = financeService.loadAttendanceDataFromCSV(OOP.SystemIT.ATTENDANCE_CSV);
            
            for (String[] row : allData) {
                if (row.length >= 4 && row[0].equals(employeeID)) {
                    String dateStr = row[1];
                    try {
                        // Parse date and check if it matches selected month
                        java.time.LocalDate date = java.time.LocalDate.parse(dateStr, java.time.format.DateTimeFormatter.ofPattern("yyyy-MM-dd"));
                        if (date.getMonthValue() == monthIndex) {
                            model.addRow(row);
                        }
                    } catch (Exception e) {
                        // Skip invalid date formats
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading attendance data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Auto-calculates payroll values based on current form inputs.
     * This method is called when the user selects an employee or changes the month.
     */
    private void autoCalculate() {
        try {
            // Get current values from form
            String employeeID = employeeIDField.getText().trim();
            String basicSalaryStr = basicSalaryField.getText().trim();
            String pagIbigStr = pagIbigField.getText().trim();
            String sssStr = sssField.getText().trim();
            String philHealthStr = philHealthField.getText().trim();
            
            if (employeeID.isEmpty() || basicSalaryStr.isEmpty()) {
                return; // Not enough data to calculate
            }
            
            double basicSalary = Double.parseDouble(basicSalaryStr);
            double pagIbig = pagIbigStr.isEmpty() ? 0.0 : Double.parseDouble(pagIbigStr);
            double sss = sssStr.isEmpty() ? 0.0 : Double.parseDouble(sssStr);
            double philHealth = philHealthStr.isEmpty() ? 0.0 : Double.parseDouble(philHealthStr);
            
            // Get manual working hours if override is enabled
            double manualHours = 0.0;
            if (manualHoursOverride) {
                String hoursStr = workingHoursField.getText().trim();
                if (!hoursStr.isEmpty()) {
                    manualHours = Double.parseDouble(hoursStr);
                }
            }
            
            // Calculate payroll using PayrollService
            PayrollService.PayrollResult result = payrollService.calculatePayrollFromValues(
                basicSalary, sss, philHealth, pagIbig, manualHours, manualHoursOverride, workHoursTable);
            
            // Update form fields with results
            workingHoursField.setText(String.format("%.2f", result.getWorkingHours()));
            totalDeductionField.setText(String.format("%.2f", result.getTotalDeductions()));
            taxIncomeField.setText(String.format("%.2f", result.getTaxableIncome()));
            taxField.setText(String.format("%.2f", result.getWithholdingTax()));
            grossPayField.setText(String.format("%.2f", result.getGrossPay()));
            netPayField.setText(String.format("%.2f", result.getNetPay()));
            
        } catch (NumberFormatException e) {
            // Invalid number format - clear calculated fields
            totalDeductionField.setText("");
            taxIncomeField.setText("");
            taxField.setText("");
            grossPayField.setText("");
            netPayField.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error calculating payroll: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

